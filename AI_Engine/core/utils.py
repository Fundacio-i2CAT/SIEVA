def save_data(data, train_path: str, test_path: str, training_data: bool = False, predict: bool = False, predict_idx = ['classification_validate_dataset']):
    if training_data:
        X_train, X_test, y_train, y_test = train_test_split(data.iloc[:,0:-1], data.iloc[:,-1], test_size = 0.24, random_state = 42)
        
        Train = X_train.assign({'target: log_type' : y_train})  
        Test = X_test.assign({'target: log_type' : y_train})  

        Train.to_csv(train_path, sep = ",", header = True, index = False )
        Test.to_csv(test_path, sep = ",", header = True, index = False)

        return train_path, test_path

    else: 
        if not predict:
            train_data =  data.loc[~data['target: log-type'].isin(predict_idx)]
            train_data.to_csv(train_path, sep = ",", header = True, index = False)

        test_paths = []
        for idx in predict_idx:
            test_data = data.loc[data['target: log-type'] == idx]
            test_data.to_csv(test_path.replace('.txt', f'-{idx}.txt'), sep = ",", header = True, index = False)    
            test_paths.append(test_path.replace('.txt', f'-{idx}.txt'))
        
        print('inside else:', train_path, test_path)
        return train_path, None if not predict else None, test_paths
    


def own2mitre(own_categories_dict: dict) -> dict:
    mitre_cat_dict = {}
    for cat in own_categories_dict.keys():
        quantity_xcent = own_categories_dict[cat]
        for mapped_cat in MITRE_MAPPING[cat]:
            mitre_cat_dict[mapped_cat] = mitre_cat_dict[mapped_cat] + quantity_xcent if mapped_cat in mitre_cat_dict.keys() else 0+quantity_xcent
    return mitre_cat_dict

def __overlap(tech1_color: str, tech2_color: str):
    if tech1_color == "#8cd699" or tech2_color == "#8cd699": return "#8cd699"
    elif tech1_color == "#ffd966" or tech2_color == "#ffd966": return "#ffd966"
    return "#ed4f4f"

def __overlap2technique_sets(technique_dict1: dict, technique_dict2: dict):
    technique_dict1, technique_dict2 = dict(sorted(technique_dict1.items())), dict(sorted(technique_dict2.items()))
    result_dict = {}
    for tech1, tech2 in zip(technique_dict1.keys(),technique_dict2.keys()):
        result_dict[tech1] = __overlap(technique_dict1[tech1], technique_dict1[tech2])
    
    return result_dict


def technique_overlapping(output_dict: dict) -> dict:
    
    predict_idx, next_predict_idx = list(output_dict.keys())[0:-1], list(output_dict.keys())[1:]

    tech_dict1 = output_dict[predict_idx[0]]["Techniques"]
    tech_dict2 = output_dict[next_predict_idx[1]]["Techniques"]
    
    result_dict = __overlap2technique_sets(tech_dict1, tech_dict2)

    for next_predict_idx in list(output_dict.keys())[2:]:
        tech_dict = output_dict[next_predict_idx]["Techniques"]
        result_dict = __overlap2technique_sets(result_dict, tech_dict)
    
    return result_dict


