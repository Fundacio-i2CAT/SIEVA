def category_percent(path_to_predictions: str, data_sources: bool):
    """
    Return the % of each category in a json format 
    :param path_to_predictions: str with the path to the predictions file to calculate the %
    """
    categories_predicted = pd.read_csv(path_to_predictions).iloc[:,-1]
    categories_percentile = categories_predicted.value_counts()/len(categories_predicted)*100
    categories_dict = own2mitre(categories_percentile) if data_sources else dict(categories_percentile)
    return categories_dict


